/**
 * This enum class represents the different Movie classifications available
 * Movie pricings will be changed accordingly depending on what classification, or 'Hype' that the movie is given
 * @author Marcus Yeo, Low Zhe Kai
 * @version 1.0.0 Nov 13, 2022
 */
public enum Hype {
    /**
     * Regular movie type, the default and cheapest movie category
     */
    REGULAR, 
    /** 
     * Blockbuster movie type, the premier and more expensive movie category
     */
    BLOCKBUSTER;
}