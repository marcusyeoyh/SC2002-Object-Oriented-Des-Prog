/**
 * This enum class represents the different viewing types for movies, which will impact the price of movies
 * Possible future extensions include a 4D option if the cinema provides such a service, the implementation would be simple as we would just have to add a new enum type
 * @author Marcus Yeo, Low Zhe Kai
 * @version 1.0.0 Nov 13, 2022
 */
public enum View {
    /**
     * 2D viewing type, the default
     */
    _2D, 
    /**
     * 3D viewing type
     */
    _3D;
}
